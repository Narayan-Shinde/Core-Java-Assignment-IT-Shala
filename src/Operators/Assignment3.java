package Operators;

public class Assignment3 {

        public static void main(String[] args) {
            // Step 1: Add 8 to 2345
            int number = 2345 + 8;

            // Step 2: Divide the result by 3
            int quotient = number / 3;

            // Step 3: Take modulus of the quotient with 5
            int remainder = quotient % 5;

            // Step 4: Multiply the remainder by 5
            int finalResult = remainder * 5;

            // Step 5: Display the final result
            System.out.println("The final result is: " + finalResult);
        }


}
